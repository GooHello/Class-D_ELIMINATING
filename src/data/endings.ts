export function selectEnding(humanityScore: number): Ending {
  if (humanityScore >= 80) return ENDINGS[0]; // A: 觉醒
  if (humanityScore >= 50) return ENDINGS[1]; // B: 服从
  if (humanityScore >= 20) return ENDINGS[2]; // C: 替代
  return ENDINGS[3]; // D: 虚无
}

export function getMidgameEnding(id: string): Ending | undefined {
  return ENDINGS.find(e => e.id === id && e.isMidgame);
}

